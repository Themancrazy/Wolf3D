static int        map_drawing(t_game game)
{
    (void)map;
    void *new_image;
    new_image = mlx_new_image(game.mlx, 15, 15) ;
    mlx_put_image_to_window(game.mlx, game.window, new_image, 1, 1);
    // mlx_get_color_value(game.mlx, 0xFFFFFF);
    return (0);
}

void        game_init(t_game game)
{
    game.mlx = mlx_init();
	game.window = mlx_new_window(game.mlx, SCREEN_WIDTH, SCREEN_HEIGTH, "Wolf3D");
	mlx_key_hook(game.window, key_handle, &game);
    mlx_loop_hook(game.mlx, map_drawing, &game);
	mlx_loop(game.mlx);
}

